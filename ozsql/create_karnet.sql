# Napravi tabelu karneta za obracun zarade

DROP TABLE IF EXISTS karnet;

CREATE TABLE karnet (
   zaposleniID SMALLINT UNSIGNED NOT NULL,
   obracunID SMALLINT UNSIGNED NOT NULL,
   koeficijent DECIMAL(4,2) NOT NULL DEFAULT 0,
   satiRadni SMALLINT UNSIGNED NOT NULL DEFAULT 0,
   tipCenaRada TINYINT UNSIGNED NOT NULL DEFAULT 1,   
   cenaRada DECIMAL(8,2) NOT NULL DEFAULT 0,
   tipRadniUcinak TINYINT UNSIGNED DEFAULT 1,
   iznosRadniUcinak DECIMAL(8,2) NOT NULL DEFAULT 0,
   satiRadPraznik SMALLINT UNSIGNED NOT NULL DEFAULT 0,
   satiRadNoc SMALLINT UNSIGNED NOT NULL DEFAULT 0,
   satiRadPraznikNoc SMALLINT UNSIGNED NOT NULL DEFAULT 0,
   satiPrekovremeno SMALLINT UNSIGNED NOT NULL DEFAULT 0,
   satiSmena SMALLINT UNSIGNED NOT NULL DEFAULT 0,
   satiNedelja SMALLINT UNSIGNED NOT NULL DEFAULT 0,
   koefMinuliRad DECIMAL(4,2) NOT NULL DEFAULT 0,
   tipNagrada TINYINT UNSIGNED DEFAULT 1,
   iznosNagrada DECIMAL(8,2) NOT NULL DEFAULT 0,
   iznosOtpremnina DECIMAL(10,2) NOT NULL DEFAULT 0,
   iznosTOPoDanu DECIMAL(8,2) NOT NULL DEFAULT 0,
   daniTO TINYINT UNSIGNED NOT NULL DEFAULT 0,
   iznosRegres DECIMAL(8,2) NOT NULL DEFAULT 0,
   iznosTerenskiDodatak DECIMAL(8,2) NOT NULL DEFAULT 0,
   iznosPoklonDeci DECIMAL(8,2) NOT NULL DEFAULT 0,
   opisOstalo VARCHAR(120),
   iznosOstalo DECIMAL(10,2) NOT NULL DEFAULT 0,
   prosek3Meseca DECIMAL(8,2) NOT NULL DEFAULT 0,
   satiGO SMALLINT UNSIGNED NOT NULL DEFAULT 0,
   satiPlacenoOdsustvo SMALLINT UNSIGNED NOT NULL DEFAULT 0,
   satiOdsustvoPraznik SMALLINT UNSIGNED NOT NULL DEFAULT 0,
   satiBolDo30D SMALLINT UNSIGNED NOT NULL DEFAULT 0,
   satiBolDo30D100Posto SMALLINT UNSIGNED NOT NULL DEFAULT 0,
   satiBolPreko30D SMALLINT UNSIGNED NOT NULL DEFAULT 0,
   satiPrekid SMALLINT UNSIGNED NOT NULL DEFAULT 0,
   satiVojnaVezba SMALLINT UNSIGNED NOT NULL DEFAULT 0,
   satiPozivDrzOrgana SMALLINT UNSIGNED NOT NULL DEFAULT 0,
   tipUmanjenja TINYINT UNSIGNED NOT NULL DEFAULT 1,
   PRIMARY KEY (zaposleniId, obracunId)
);
